# example of running a while loop on multiple variables

mystery_int_1 = 1
mystery_int_2 = 5
mystery_int_3 = 6

while mystery_int_1 > 0 or mystery_int_2 > 0 or mystery_int_3 > 0:
    mystery_int_1 -= 1
    mystery_int_2 -= 1
    mystery_int_3 -= 1
    print(mystery_int_1, mystery_int_2, mystery_int_3)
